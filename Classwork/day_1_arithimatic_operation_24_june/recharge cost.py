# Program to calculate total recharge cost

# Input: Cost per GB and Number of GBs
cost_per_gb = float(input("Enter the cost per GB: "))
num_gb = int(input("Enter the number of GBs: "))

# Calculate total recharge cost
total_cost = cost_per_gb * num_gb

# Output: Total Recharge Cost
print("Total Recharge Cost:", total_cost)

