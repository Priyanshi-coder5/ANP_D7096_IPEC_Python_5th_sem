# Program to remove an element from a list using the remove() method





#-------------coding-----------------------
numbers = [40, 50, 60, 70, 80, 90, 100, 110, 120, 130]
print ("numbers:", numbers)
print ("-----------------------------")
#removing from list using index
numbers.remove(90)
print ("numbers after removing 90:", numbers)
print("after removing 90:", numbers)
#----------output-----------------------
'''--------------------
numbers are [40, 50, 60, 70, 80, 100, 110, 120, 130]
after removing 90: [40, 50, 60, 70, 80, 100, 110, 120, 130]
----------------------'''