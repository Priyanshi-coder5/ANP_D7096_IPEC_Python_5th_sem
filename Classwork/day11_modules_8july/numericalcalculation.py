#function to calculation addition of two numbers 
def calculation_addition(num1, num2):
    return num1 + num2

#function to calculation subtraction of two numbers 
def calculation_subtraction(num1, num2):
    return num1 - num2

#function to calculation multiplication of two numbers 
def calculation_multiplication(num1, num2):
    return num1 * num2

#function to calculation division of two numbers 
def calculation_division(num1, num2):

    return num1 / num2  
#function to calculation modulus of two numbers
def calculation_modulus(num1, num2):
    return num1 % num2